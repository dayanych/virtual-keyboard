document.onkeydown = function (event) {
  // event.key
  document.querySelectorAll('[data]').forEach((element) => {
    element.classList.remove('active');
  });
  const char = document.querySelector(`[data="${event.key}"]`);
  char.classList.add('active');
};

const drawKeyboard = (arr) => {
  const keyboard = document.createElement('div');
  keyboard.className = 'keyboard';
  const keyboardLayout = arr.reduce((acc, curr, index) => {
    if (['Tab', 'CapsLock', 'Shift', 'Control'].includes(curr) && index !== arr.length - 1 && index !== 54) {
      acc.push([curr]);
    } else {
      acc[acc.length - 1].push(curr);
    }
    return acc;
  }, [[]]);
  for (let i = 0; i < keyboardLayout.length; i++) {
    const row = document.createElement('div');
    row.className = 'row';
    for (let j = 0; j < keyboardLayout[i].length; j++) {
      row.innerHTML += `<div class="char" data="${keyboardLayout[i][j]}">${keyboardLayout[i][j]}</div>`;
    }
    keyboard.appendChild(row);
  }
  document.body.prepend(keyboard);
};

